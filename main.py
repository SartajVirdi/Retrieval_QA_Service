from flask import Flask, request, jsonify
import cohere
import fitz  # PyMuPDF
import faiss
import numpy as np
import os

app = Flask(__name__)

# Load your Cohere API key securely using an environment variable
co = cohere.Client(os.getenv("COHERE_API_KEY"))

# Load and chunk the PDF content (do this once at startup or cache)
def load_pdf(path):
    doc = fitz.open(path)
    return "\n".join(page.get_text() for page in doc)

text = load_pdf("policy.pdf")  # Replace with actual filename if needed

def split_text(text, max_tokens=300):
    import re
    sentences = re.split(r'(?<=[.!?])\s+', text)
    chunks, chunk = [], ""
    for sentence in sentences:
        if len(chunk) + len(sentence) <= max_tokens:
            chunk += " " + sentence
        else:
            chunks.append(chunk.strip())
            chunk = sentence
    if chunk:
        chunks.append(chunk.strip())
    return chunks

chunks = split_text(text)

# Embed and index the chunks
embed_response = co.embed(texts=chunks, model="embed-english-v3.0", input_type="search_document")
embeddings = np.array(embed_response.embeddings).astype("float32")
index = faiss.IndexFlatL2(embeddings.shape[1])
index.add(embeddings)

def search(query, k=3):
    query_embed = co.embed(texts=[query], model="embed-english-v3.0", input_type="search_query").embeddings[0]
    D, I = index.search(np.array([query_embed], dtype="float32"), k)
    return [chunks[i] for i in I[0]]

@app.route("/webhook", methods=["POST"])
def webhook():
    data = request.get_json()
    if not data or "query" not in data:
        return jsonify({"error": "Query field missing"}), 400

    query = data["query"]
    retrieved = search(query)
    context = "\n".join(retrieved)

    prompt = f"""Based on the following content, answer the question below:

Content:
{context}

Question: {query}
Answer:"""

    response = co.chat(
        model='command-r-plus',
        message=query,
        documents=[{"text": context}]
    )

    return jsonify({"response": response.text.strip()})

if __name__ == "__main__":
    app.run(host="0.0.0.0", port=5000)